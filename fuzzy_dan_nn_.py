    # Perhitungan untuk Exhaust Fan dan LED Indikator berdasarkan Kadar Gas Ammonia
    fan_led_sim.input['GasAmmonia'] = row['GasAmmonia']
    fan_led_sim.compute()

    # Menyimpan hasil output untuk Exhaust Fan dan LED Indikator
